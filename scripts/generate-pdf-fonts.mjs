/**
 * PDF용 Pretendard 폰트 서브셋 생성 스크립트.
 * 사용법: node scripts/generate-pdf-fonts.mjs
 *
 * 출력:
 *   src/lib/pdf/fonts/pretendard-regular.ts
 *   src/lib/pdf/fonts/pretendard-bold.ts
 *
 * 서브셋 범위:
 *   U+0020-007E (Basic Latin)
 *   U+AC00-D7A3 (Hangul Syllables 11,172자)
 *   U+3130-318F (Hangul Compatibility Jamo)
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const subsetFont = require("subset-font");

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

// Build the character set string
function buildCharSet() {
  let chars = "";
  // Basic Latin U+0020-007E
  for (let i = 0x0020; i <= 0x007e; i++) chars += String.fromCodePoint(i);
  // Hangul Compatibility Jamo U+3130-318F
  for (let i = 0x3130; i <= 0x318f; i++) chars += String.fromCodePoint(i);
  // Hangul Syllables U+AC00-D7A3
  for (let i = 0xac00; i <= 0xd7a3; i++) chars += String.fromCodePoint(i);
  return chars;
}

async function generateFont(inputPath, outputPath, exportName) {
  console.log(`Reading ${inputPath}...`);
  const inputBuffer = readFileSync(inputPath);
  const charSet = buildCharSet();

  console.log(`Subsetting ${charSet.length} characters...`);
  const subsetBuffer = await subsetFont(inputBuffer, charSet, {
    targetFormat: "truetype",
  });

  const base64 = subsetBuffer.toString("base64");
  const tsContent = `// Auto-generated by scripts/generate-pdf-fonts.mjs — do not edit manually
export const ${exportName} = "${base64}";
`;
  writeFileSync(outputPath, tsContent, "utf-8");
  const sizeKB = Math.round(base64.length / 1024);
  console.log(`Written ${outputPath} (${sizeKB} KB base64)`);
}

async function main() {
  const regularTtf = resolve(
    ROOT,
    "node_modules/pretendard/dist/public/static/alternative/Pretendard-Regular.ttf"
  );
  const boldTtf = resolve(
    ROOT,
    "node_modules/pretendard/dist/public/static/alternative/Pretendard-Bold.ttf"
  );
  const regularOut = resolve(ROOT, "src/lib/pdf/fonts/pretendard-regular.ts");
  const boldOut = resolve(ROOT, "src/lib/pdf/fonts/pretendard-bold.ts");

  await generateFont(regularTtf, regularOut, "PRETENDARD_REGULAR_BASE64");
  await generateFont(boldTtf, boldOut, "PRETENDARD_BOLD_BASE64");
  console.log("Done! Both font files generated.");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
